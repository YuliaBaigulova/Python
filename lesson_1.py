# (прибыль — выручка больше издержек, или убыток — издержки больше выручки). Выведите соответствующее сообщение.
# Если фирма отработала с прибылью, вычислите рентабельность выручки (соотношение прибыли к выручке).
# Далее запросите численность сотрудников фирмы и определите прибыль фирмы в расчете на одного сотрудника.
revenue = int(input("Введите сумму выручки, руб. >>>"))
costs = int(input("Введите сумму издержек, руб. >>>"))
employees = int(input("Введите количество сотрудников Вашей фирмы >>>"))
profit = revenue - costs
if revenue > costs:
    print(f"Ваша прибыль составила: {profit} руб.")
    print(f"Рентабельность фирмы: {profit / revenue}")
    print(f"Удельная прибыль на каждого сотрудника: {profit / employees} руб.")
