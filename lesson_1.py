# 4. Пользователь вводит целое положительное число. Найдите самую большую цифру в числе.
# Для решения используйте цикл while и арифметические операции.
n = input("Ведите любое целое положительное число >>>")
i = 0
max = str("0")
while i < len(n):
    if max < n[i]:
        max = n[i]
    i += 1
print(f"Самая большая цифра в веденном Вами числе: {max}")

